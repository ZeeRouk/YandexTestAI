            model="llama3-8b-8192",  # –°—Ç–∞–±–∏–ª—å–Ω–∞—è –º–æ–¥–µ–ª—å –±–µ–∑ —Ç—Ä–µ–±–æ–≤–∞–Ω–∏–π –∫ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—é —Ç–µ–ª–µ—Ñ–æ–Ω–∞
